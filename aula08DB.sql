                                         GERENCIANDO CÓPIAS DE SEGURANÇA MYSQL
--> EXPORTAR DB <--

1 --> SERVER
2 --> DATA EXPORT
3 --> DUMP STRUCTURE AND DATA
4 --> EXPORT TO SELF-CONTAINED FILE
5 --> INCLUDE CREATE SCHEMA
6 --> START EXPORT
7 --> SENHA VAZIA
8 --> ARQUIVO DUMP SALVADO EM PASTA DUMPS




--> IMPORTAR DB <--

1 --> SERVER
2 --> DATA IMPORT
3 --> IMPORT FROM SELF-CONTAINED FILE
4 --> SELECIONAR ARQUIVO DUMP
5 --> START IMPORT
6 --> SENHA VAZIA
7 --> REFRESH
8 --> DAR O COMANDO "USE NOME_BANCO" E "SHOW TABLES" PARA VERIFICAR SE OS DADOS FORAM SALVOS
